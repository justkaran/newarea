                    <h5>code4sp Espinho</h5>
                    <p>The EU-funded project code4sp has been running since January 2021, but it was not until this October that the participating organisations from Portugal, Greece and Cyprus as well as Germany were able to meet in a common location. Meeting point was the Portuguese coastal town of Espinho which is close enough to Porto airport to be easily accessible for all - although the Spanish colleague, who works remotely for a Greek organisation, still decided to travel the distance by car and thus get to know more of Europe.
                    </p>
                    <p>The aim of the code4sp project is to spread the CodeDoor system further in the Mediterranean countries. In this way, even more people should be able to obtain simple and sustainable digital qualifications through their NGOs.
                        With the sound of the surf in their ears, the organisational staff exchanged their project results so far. In the participating countries, the first working groups with several national NGOs have already taken place, debating the opportunities and obstacles that arise from the use of the CodeDoor platform. CodeDoor staff members were also present and impressed by how intensively the organisations are involved with CodeDoor and the learning system.
                    </p>
                    <p>CodeDoor was able to answer many open questions on the spot and provide advice and support to the organisations: In the six years that CodeDoor has been operating, a lot of experience has already been gained. At the same time, it was particularly inspiring and helpful for the team to hear the thoughts of NGOs that have never worked with CodeDoor before.
                    </p>
                    <p>During a break, the guests were guided through the classrooms of the hosts. Here, learners not only produce delicious pastries but also steel work, 3-D models, IT projects or irrigation systems.
                    </p>
                    <p>After this successful first meeting, the next exchange will take place online again for the time being. Now the focus will be on how CodeDoor will be implemented in the countries in order to fulfill their individual requirements.
                    </p>


